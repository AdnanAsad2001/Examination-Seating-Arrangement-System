    private void absenteeList_MP(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_absenteeList_MP
        if (jList4.getSelectedValue() != null) {
            String studentregno = jList4.getSelectedValue().toString();

            ResultSet r = DB.executeQuery("select * from absentees where studentregno = '" + studentregno + "' and examid = " + currentAttendanceExamID);
            try {
                r.next();
                jTextField2.setText(studentregno);
                absenteeID = r.getString("id");
                r = DB.executeQuery("select name from students where regno='" + studentregno + "'");
                r.next();
                jTextField1.setText(r.getString("name"));
            } catch (Exception ex) {
                Logger.getLogger(AdminWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
            finally{
                DB.flushStatementOnly();
            }
        }
}//GEN-LAST:event_absenteeList_MP

    private void search_actionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_actionPerformed
        try {
            ResultSet r = null;

            if (!jTextField3.getText().isEmpty()) {
                r = DB.executeQuery("select * from absentees where studentregno like '" + jTextField3.getText() + "%' and examid=" + currentAttendanceExamID);
            } else {
                r = DB.executeQuery("select * from absentees where examid=" + currentAttendanceExamID);
            }
            Vector v = new Vector();
            while (r != null && r.next()) {
                v.add(r.getString("studentregno"));
            }
            jList4.setListData(v);
            DB.flushStatementOnly();
        } catch (SQLException ex) {
            Logger.getLogger(AdminWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally{
            DB.flushStatementOnly();
        }
    }//GEN-LAST:event_search_actionPerformed

