    private void removeAbsentee_MP(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeAbsentee_MP
        if (!absenteeID.isEmpty()) {
            if (JOptionPane.showConfirmDialog(rootPane, "Delete This Absentee?", "Absentee Delete", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                if (DB.executeUpdate("delete from absentees where id = " + absenteeID)) {
                    JOptionPane.showMessageDialog(rootPane, "Absentee Deleted");
                    DB.flushStatementOnly();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Absentee Deletion Failed !!!");
                }
            }
            loadDataToFields();
        } 
        else {
            JOptionPane.showMessageDialog(rootPane, "Select a Absentee !!!");
        }
}//GEN-LAST:event_removeAbsentee_MP
