    private void loadDataToField() throws SQLException {
        ResultSet r = DB.executeQuery("select studentregno from nonregisteredstudents where examid=" + SeatingArranger.ExamID );
        Vector v = new Vector();
        while (r != null && r.next()) {
            v.add(r.getString("studentregno"));
        }
        jList1.setListData(v);
        DB.flushStatementOnly();
    }

