    String currentStudentID = "";
    private void nonRegListMP(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_nonRegListMP
        if (jList1.getSelectedValue() != null) {
            currentStudentID = jList1.getSelectedValue().toString();

            ResultSet r = DB.executeQuery("select * from students where id = " + currentStudentID);
            try {
                if (r != null && r.next()) {
                    jTextField3.setText(r.getString("name"));
                    jTextField4.setText(r.getString("regno"));
                    jTextField5.setText(r.getString("batchid"));
                    jTextField6.setText(r.getString("deptid"));
                }
            } 
            catch (Exception ex) {
                Logger.getLogger(AdminWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
            finally{
                DB.flushStatementOnly();
            }
        }
    }//GEN-LAST:event_nonRegListMP

    private void removeFromListMP(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeFromListMP
        if (!currentStudentID.isEmpty()) {
            // storing previous values to a vector ..
            if (JOptionPane.showConfirmDialog(rootPane, "Remove this Student from The Non Registered List?", "Remove Student", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                if (DB.executeUpdate("delete from nonregisteredstudents where studentregno='"+currentStudentID+"' and examid="+SeatingArranger.ExamID )) {
                    JOptionPane.showMessageDialog(rootPane, "Student Removed");
                } 
                else {
                    JOptionPane.showMessageDialog(rootPane, "Student Removal Failed !!!");
                }
            }
        } 
        else {
            JOptionPane.showMessageDialog(rootPane, "Select Student To Be Removed");
        }
    }//GEN-LAST:event_removeFromListMP

